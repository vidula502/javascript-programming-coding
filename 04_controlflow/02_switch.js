const month = 2;

switch (month) {
    case 1:
        console.log('January 2023');
        break;
    case 2:
        console.log('February 2023');
        break;
    case 3:
        console.log('March 2023');
        break;
    case 4:
        console.log('April 2023');
        break;
    case 5:
        console.log('May 2023');
        break;
    default:
        console.log('June 2023');
        break;
}